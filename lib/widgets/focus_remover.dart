import 'package:flutter/material.dart';

class FocusRemover extends StatelessWidget {
  final Widget child;

  FocusRemover({
    Key key,
    @required this.child,
  })  : assert(child != null),
        super(key: key);

  @override
  Widget build(BuildContext context) {
    return GestureDetector(
      onTap: () {
        FocusScope.of(context).requestFocus(FocusNode());
      },
      child: child,
    );
  }
}
